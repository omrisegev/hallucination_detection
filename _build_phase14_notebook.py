"print(f'{\"VC (reproduced)\":<32} {\"K=1\":<4} {\"black-box\":<12} {vc_auc:>7.3f}  {REF_VC_K2:>8.3f}')\n"
"print(f'{\"SC (reproduced)\":<32} {\"K=2\":<4} {\"black-box\":<12} {sc_auc:>7.3f}  {REF_SC_K2:>8.3f}')\n"
"print(f'{\"SCVC (reproduced)\":<32} {\"K=2\":<4} {\"black-box\":<12} {scvc_auc:>7.3f}  {REF_SCVC_K2:>8.3f}')\n"
